package Operators;

public class Arithmeticademo {

    public static void main(String[] args) {

        int i = 10;
        int j = 45;

        System.out.println(j+i);
        System.out.println(j-i);
        System.out.println(j*i);
        System.out.println(j/i);
        System.out.println(j%i);

        System.out.println(10 + 20 - 5 * 2 + 1 + 30/10);

        //BODMAS --> Brackets,power off, division, multiplication, Addition, subtraction




    }
}
