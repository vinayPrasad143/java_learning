package condiationstatements;

public class switchcondition4 {

    public static void main(String[] args) {

        int i= 100;

        switch (i)
        {
            case 2:
                System.out.println(i);
                break;

            case 4:
                System.out.println(i);
                break;

            case 6:
                System.out.println(i);
                break;

            case 10:
                System.out.println(i);
                break;

            default:
                System.out.println("Nothing");

        }
    }
}
